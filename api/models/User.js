// here we will make a user schema with the help of mogoose library ... 
const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    profilePic: {
      type: String,
      default: "",
    },
  },
  { timestamps: true }
);
// time stamps is for creating a date updated at and created at times .... 

module.exports = mongoose.model("User", UserSchema);